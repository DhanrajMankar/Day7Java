package UC1;

public class Gambler {
	
	private int stake=100;
	private int totalDays;
	private int winDay;
	private int loseDay;
	public int getStake() {
		return stake;
	}
	public void setStake(int stake) {
		this.stake = stake;
	}
	public int getTotalDays() {
		return totalDays;
	}
	public void setTotalDays(int totalDays) {
		this.totalDays = totalDays;
	}
	public int getWinDay() {
		return winDay;
	}
	public void setWinDay(int winDay) {
		this.winDay = winDay;
	}
	public int getLoseDay() {
		return loseDay;
	}
	public void setLoseDay(int loseDay) {
		this.loseDay = loseDay;
	}
	
	

}
